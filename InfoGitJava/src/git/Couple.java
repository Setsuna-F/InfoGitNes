package git;

public class Couple {
	
	private String refBranch1;
	private String refBranch2;
	
	
	/**
	 * \brief constructeur
	 * 
	 * \param refb1  reference de la branche 1.
	 * \param refb2  reference de la branche 2.
	 * 
	 **/
	public Couple(String refb1, String refb2) {
		refBranch1 = refb1;
		refBranch2 = refb2;
	}
}
